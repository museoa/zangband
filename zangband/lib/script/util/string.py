#####################################################################
#
# Convert an integer to a char (0 = 'a', 1 = 'b', ...)
#
#####################################################################
def I2A(x):
	return x + ord('a')


#####################################################################
#
# Convert a char to an integer ('a' = 0, 'b' = 1, ...)
#
#####################################################################
def A2I(x):
	return ord(x) - ord('a')

